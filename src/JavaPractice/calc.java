package JavaPractice;

public class calc {

	public static void main(String[] args) {
		// 算術演算子 + - * / %
		int a = 15;
		int b = 4;
		System.out.println(a+b);
//【重要】↓のように出力ソースコードと"\n"をくわえることで改行も可能。
		System.out.println("\n");
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
//　%は除算（余り）をだす方法
		System.out.println(a%b);
		
	}

}
